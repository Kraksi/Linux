#!/bin/bash

# Проверяем, переданы ли два аргумента
if [ "$#" -ne 2 ]; then
  echo "Ошибка: требуется ввести два числа."
  echo "Использование: $0 <число1> <число2>"
  exit 1
fi

# Проверяем, что оба аргумента являются числами
if ! [[ "$1" =~ ^-?[0-9]+$ ]] || ! [[ "$2" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: оба аргумента должны быть числами."
  exit 1
fi

# Суммируем два числа
sum=$(( $1 + $2 ))

# Вывод результата
echo "Сумма чисел $1 и $2 равна $sum"
