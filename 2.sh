#!/bin/bash

# Проверяем, был ли передан аргумент
if [ "$#" -ne 1 ]; then
  echo "Ошибка: требуется передать одно число."
  echo "Использование: $0 <число>"
  exit 1
fi

# Проверяем, является ли переданный аргумент числом
if ! [[ "$1" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: аргумент должен быть числом."
  exit 1
fi

# Определяем, четное ли число
if [ $(( $1 % 2 )) -eq 0 ]; then
  echo "Число $1 является четным."
else
  echo "Число $1 является нечетным."
fi
