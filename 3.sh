#!/bin/bash

# Проверяем, что файл передан как аргумент
if [ "$#" -ne 1 ]; then
  echo "Ошибка: требуется указать имя файла."
  echo "Использование: $0 <имя_файла>"
  exit 1
fi

# Проверяем, существует ли файл
if [ ! -f "$1" ]; then
  echo "Ошибка: файл '$1' не найден."
  exit 1
fi

# Запрашиваем номер строки у пользователя
read -p "Введите номер строки для вывода: " line_number

# Проверяем, является ли номер строки числом
if ! [[ "$line_number" =~ ^[0-9]+$ ]]; then
  echo "Ошибка: номер строки должен быть положительным числом."
  exit 1
fi

# Выводим указанную строку из файла
sed -n "${line_number}p" "$1"
