#!/bin/bash

# Проверяем, что директория передана как аргумент
if [ "$#" -ne 1 ]; then
  echo "Ошибка: требуется указать директорию."
  echo "Использование: $0 <директория>"
  exit 1
fi

DIRECTORY="$1"

# Проверяем, существует ли директория
if [ ! -d "$DIRECTORY" ]; then
  echo "Ошибка: директория '$DIRECTORY' не существует."
  exit 1
fi

# Находим и удаляем файлы старше 5 дней
find "$DIRECTORY" -type f -mtime +5 -exec rm -v {} \;

echo "Все файлы старше 5 дней в директории '$DIRECTORY' удалены."
